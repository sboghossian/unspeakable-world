/**
 * galaxy-catalog — hand-curated list of 40 well-known galaxies that are
 * recognisable to anyone who has ever owned an astronomy book. Each row
 * carries enough parameters to drive the procedural shader pipeline
 * (`galaxy-mesh.ts`): morphology bucket, apparent size, axis ratio,
 * inclination, spiral arm count + pitch angle.
 *
 * Source columns (per row, after the catalog):
 *   • RA / Dec (ICRS J2000), distance, magnitude          — NED / SIMBAD
 *   • Hubble type / morphology bucket                     — de Vaucouleurs
 *                                                           classifications
 *                                                           via NED
 *   • Physical size (kpc, D25 isophotal)                  — RC3 + HyperLeda
 *   • Inclination, axis ratio                             — HyperLeda
 *   • Arm count / spiral pitch                            — Ringermacher &
 *                                                           Mead 2009, Davis
 *                                                           et al. 2012 (P2DFFT
 *                                                           sample), individual
 *                                                           paper citations
 *                                                           where indicated
 *
 * Where a primary literature value was unavailable, we use the
 * NED/HyperLeda best estimate; where multiple papers disagree (M101's
 * pitch is variously quoted at 18°-21°, Cen A's "type" depends on which
 * decade you read), we picked the most-cited recent value. These
 * numbers drive a stylised rendering — they don't need sub-arcsec
 * accuracy.
 */

export type GalaxyType =
  | "spiral"
  | "barred-spiral"
  | "elliptical"
  | "edge-on"
  | "irregular"
  | "sombrero"
  | "ring"
  | "peculiar";

export type GalaxyRow = {
  /** Unique stable id (folded into the mesh name for picking). */
  readonly id: string;
  /** Display name. */
  readonly name: string;
  /** SIMBAD-resolvable canonical identifier. */
  readonly simbadId: string;
  /** ICRS J2000 right ascension, degrees. */
  readonly raDeg: number;
  /** ICRS J2000 declination, degrees. */
  readonly decDeg: number;
  /** Distance, megaparsecs. */
  readonly distanceMpc: number;
  /** Morphological bucket driving shader selection. */
  readonly galaxyType: GalaxyType;
  /**
   * Inclination, degrees. 0 = face-on, 90 = edge-on. Used to tilt the
   * disc and pick `edge-on` shading.
   */
  readonly inclinationDeg: number;
  /** Minor/major axis ratio (b/a), 0.05-1.0. */
  readonly axisRatio: number;
  /** Physical D25 isophotal diameter, kpc. */
  readonly sizeKpc: number;
  /** Number of major spiral arms (0 for non-spirals). */
  readonly armCount: number;
  /** Spiral pitch angle, degrees. 0 for non-spirals. */
  readonly spiralPitchDeg: number;
  /** Apparent integrated visual magnitude (V or B, approximate). */
  readonly magnitude: number;
  /** Short note for the future hover card. */
  readonly note: string;
};

